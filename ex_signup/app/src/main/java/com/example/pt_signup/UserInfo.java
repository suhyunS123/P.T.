package com.example.pt_signup;

public class UserInfo {
    private String UID;
    private String pw;
    private String name;
    public String getUID()
    {
        return UID;
    }
    public void setUID(String UID)
    {
        this.UID = UID;
    }
    public String getPw()
    {
        return pw;
    }
    public void setPw(String pw)
    {
        this.pw = pw;
    }
    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }
}
